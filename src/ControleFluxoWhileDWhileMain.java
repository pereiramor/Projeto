public class ControleFluxoWhileDWhileMain {

    public static void  main(String[] args){
        //exemplo com while
        int x = 01;
        while (x<10){
            System.out.println("item" + x);
            x++;
        }
        //exemplo com do-while
        int y = 10;
        do {
            System.out.println("item" + y);
            y++;
        } while (y <20) ;
        }
    }

